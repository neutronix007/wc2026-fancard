// Helper: get a random surname for a country key
function getRandomSurname(countryKey) {
  const data = COUNTRY_SURNAMES[countryKey];
  if (!data) return '';
  const list = data.surnames;
  return list[Math.floor(Math.random() * list.length)];
}
